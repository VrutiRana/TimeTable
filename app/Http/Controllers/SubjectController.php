    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        for ($i = 0; $i < $request->totalSubjects; $i++){
            $data = new Subject();
            $data->weekday_id = $request->weekdayid;
            $data->subject=$request->subject[$i];
            $data->hours_of_subjects=$request->hours[$i];
            $data->save();
        }
        $weekdays = WorkingHours::where('id',$request->weekdayid)->first();
        $subjects = Subject::where('weekday_id',$request->weekdayid)->get();
        return view('subjectlist',['subjects'=>$subjects,'weekdays'=>$weekdays]);
    }


